package com.gn.homework08.closed.model.vo;

public class Person {
	private String name;
	private int age;
	
	public Person() {}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
}
